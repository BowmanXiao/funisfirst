'''
题目：809*??=800*??+9*?? 其中??代表两位数, 809*??为四位数，8*??的结果为两位数，9*??的结果为3位数。求??代表的两位数，及809*??后的结果。

程序分析：无。
'''
# 方法一
i = 10
while i < 100:
    if 809 * i > 1000 and 809 * i < 10000 and 8 * i < 100 and 9 * i > 100 and 9 * i < 1000:
        if 809 * i == 800 * i + 9 * i:
            print(f'809*{i}=800*{i}+9*{i}={809*i}')
            break
    i += 1

