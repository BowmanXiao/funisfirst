'''
题目：一个5位数，判断它是不是回文数。即12321是回文数，个位与万位相同，十位与千位相同。

程序分析：无。
'''

# 方法一
num = input("请输入一个数：")
if num == num[::-1]:
    print('%s是回文数' % num)
else:
    print('%s不是回文数' % num)

